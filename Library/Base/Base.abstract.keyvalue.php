<?php

abstract class Base_abstract_keyvalue
{

    protected string $prefix = 'keyValue';
    protected string $class ='';
    protected string $key          = '';
    protected string $value        = '';
    protected string $valueDefault ='';

    /**
     * @return string
     */
    public function getPrefix(): string
    {
        return $this->prefix;
    }

    /**
     * @param string $prefix
     */
    public function setPrefix(string $prefix): void
    {
        $this->prefix = $prefix;
    }

    /**
     * @return string
     */
    public function getClass(): string
    {
        return $this->class;
    }

    /**
     * @param string $class
     */
    public function setClass(string $class): void
    {
        $this->class = $class;
    }

    /**
     * @return string
     */
    public function getKey(): string
    {
        return $this->key;
    }

    /**
     * @param string $key
     */
    public function setKey(string $key): void
    {
        $this->key = $key;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }

    /**
     * @param string $value
     */
    public function setValue(string $value): void
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValueDefault(): string
    {
        return $this->valueDefault;
    }

    /**
     * @param string $valueDefault
     */
    public function setValueDefault(string $valueDefault): void
    {
        $this->valueDefault = $valueDefault;
    }

}
